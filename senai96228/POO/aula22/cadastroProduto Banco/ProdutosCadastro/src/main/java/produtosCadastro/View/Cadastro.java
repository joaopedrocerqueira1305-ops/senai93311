    private void BtnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRemoverActionPerformed
        try {
            int linha = tbListar.getSelectedRow();

            if (linha == -1) {
                javax.swing.JOptionPane.showMessageDialog(
                    this,
                    "Selecione uma pessoa na tabela para remover."
                );
                return;
            }

            int id = Integer.parseInt(
                tbListar.getValueAt(linha, 0).toString()
            );

            int resposta = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Deseja realmente remover este cadastro?",
                "Confirmar remoção",
                javax.swing.JOptionPane.YES_NO_OPTION
            );

            if (resposta == javax.swing.JOptionPane.YES_OPTION) {
                controle.remover(id);

                atualizarTabela();

                txtNome.setText("");
                txtCategoria.setText("");
                txtPreco.setText("");
                txtQuantidade.setText("");

                javax.swing.JOptionPane.showMessageDialog(
                this,
                "Cadastro removido com sucesso!"
                );
            }

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(
            this,
            "Erro ao remover: " + e.getMessage()
            );
        }
    }//GEN-LAST:event_BtnRemoverActionPerformed

    private void BtnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCadastrarActionPerformed
        try {
            String nome = txtNome.getText().trim();
            String categoria = txtCategoria.getText().trim();
            double preco = Double.parseDouble(txtPreco.getText().trim());
            int quantidade = Integer.parseInt(txtQuantidade.getText().trim());

            if (modoAtualizacao == true) {

                controle.atualizar(idSelecionado, nome, categoria, preco, quantidade);

                javax.swing.JOptionPane.showMessageDialog(
                this,
                "Cadastro alterado com sucesso!"
                );

                modoAtualizacao = false;
                idSelecionado = -1;
                BtnCadastrar.setText("ENTRAR");

            } else {

                Produto p = new Produto();
                p.setNome(nome);
                p.setCategoria(categoria);
                p.setPreco(preco);
                p.setQuantidade(quantidade);

                controle.cadastrar(p);

                javax.swing.JOptionPane.showMessageDialog(
                this, 
                "Produto cadastrada com sucesso!"
                );
            }

            atualizarTabela();

            txtNome.setText("");
            txtCategoria.setText("");
            txtPreco.setText("");
            txtQuantidade.setText("");
            txtBusca.setText("");

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(
            this,
            "Erro: " + e.getMessage()
            );
        }
    }//GEN-LAST:event_BtnCadastrarActionPerformed

