    private void atualizarTabela() {
        tabelaModelo.setRowCount(0); // limpa os dados da tabela

        ArrayList<Produto> lista = controle.listar(); // busca do banco
        for (Produto p : lista) {
            tabelaModelo.addRow(new Object[]{p.getId(), p.getNome(), p.getCategoria(), p.getPreco(), p.getQuantidade()});
        }
        
    }
    
    private void buscarDinamicamente() {
        String nomeBuscado = txtBusca.getText().trim();

        tabelaModelo.setRowCount(0); // limpa a tabela
        ArrayList<Produto> lista = controle.buscarPorNome(nomeBuscado);

        for (Produto p : lista) {
            tabelaModelo.addRow(new Object[]{p.getId(), p.getNome(), p.getCategoria(), p.getPreco(), p.getQuantidade()});
        }
        
    }

    private void preencherCamposDaTabela() {
        int linha = tbListar.getSelectedRow();

        if (linha != -1) {
            idSelecionado = Integer.parseInt(
                tbListar.getValueAt(linha, 0).toString()
            );

            txtNome.setText(
                tbListar.getValueAt(linha, 1).toString()
            );

            txtCategoria.setText(
                tbListar.getValueAt(linha, 2).toString()
            );
            
            txtPreco.setText(
                tbListar.getValueAt(linha, 3).toString()
            );
            txtQuantidade.setText(
                tbListar.getValueAt(linha, 4).toString()
            );
        }
    }
  


